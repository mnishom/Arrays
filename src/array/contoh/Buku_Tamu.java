    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        //ambil teks dalam komponen "txtNama" kemudian simpan dalam variabel "nama"
        String nama = txtNama.getText();
        //operator ternary
        //jika komponen "rbL" dipilih, maka jenis kelamin=Laki-Laki
        //selain itu, maka jenis kelamin=Perempuan
        String jenis_kelamin = rbL.isSelected()?"Laki-Laki":"Perempuan";
        //ambil teks dalam komponen "txtKantor" kemudian simpan dalam variabel "kantor"
        String kantor = txtKantor.getText();
        //ambil teks dalam komponen "txtKeperluan" kemudian simpan dalam variabel "keperluan"
        String keperluan = txtKeperluan.getText();
        //pengecekan
        //jika variabel "nama" tidak ada isinya atau kosong
        if(nama.isEmpty()){
            //taampilkan pesan "Nama lengkap harus diisi"
            JOptionPane.showMessageDialog(this, "Nama lengkap harus diisi!");
            //setelah itu, letakkan "caret cursor" pada komponen "txtNama"
            txtNama.requestFocus();            
            
        //jika variabel "kantor" tidak ada isinya atau kosong
        }else if(kantor.isEmpty()){
            //taampilkan pesan "Kantor/Instansi harus diisi!"
            JOptionPane.showMessageDialog(this, "Kantor/Instansi harus diisi!");
            //setelah itu, letakkan "caret cursor" pada komponen "txtKantor"
            txtKantor.requestFocus();
            
        //jika variabel "keperluan" tidak ada isinya atau kosong
        }else if(keperluan.isEmpty()){
            //taampilkan pesan "Keperluan harus diisi!"
            JOptionPane.showMessageDialog(this, "Keperluan harus diisi!");
            //setelah itu, letakkan "caret cursor" pada komponen "txtKeperluan"
            txtKeperluan.requestFocus();
            
        //jika data telah terisi dengan lengkap, maka simpan data ke dalam tabel
        }else {
            //untuk membuat nomor urut = jumlah baris dalam tabel + 1
            int nomor = tabelTamu.getRowCount()+1;
            //buat array object dari data
            Object[] data = {nomor,tanggal(),nama,jenis_kelamin,kantor,keperluan};
            //menambahkan baris data ke dalam tabel
            model.addRow(data); 
        }
    }//GEN-LAST:event_btnSimpanActionPerformed
  
    
