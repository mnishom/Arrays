    private void aturTabel(){
        //mendefinisikan kolom pada JTable
        Object[] header = {"No","Tanggal","Nama Lengkap","Jenis Kelamin","Kantor/Instansi","Keperluan"};
        //instansiasi model untuk sebuah tabel
        model = new DefaultTableModel(header, 0);
        //atur model JTable menggunakan model yang telah kita
        //definisikan di atas.
        tabelTamu.setModel(model); 
        //tinggi setiap baris adalah 25px
        tabelTamu.setRowHeight(25);        
        //spasi setiap cell adalah 10
        tabelTamu.setIntercellSpacing(new Dimension(10,1));        
        //kolom pertama (nomor) ukuran panjang kolom fix=50px
        tabelTamu.getColumnModel().getColumn(0).setMinWidth(50);
        tabelTamu.getColumnModel().getColumn(0).setMaxWidth(50);
        //kolom kedua (tanggal) ukuran panjang kolom fix=100px
        tabelTamu.getColumnModel().getColumn(1).setMinWidth(100);
        tabelTamu.getColumnModel().getColumn(1).setMaxWidth(100);
        //kolom keempat (jenis kelamin) ukuran panjang kolom fix=100px
        tabelTamu.getColumnModel().getColumn(3).setMinWidth(100);
        tabelTamu.getColumnModel().getColumn(3).setMaxWidth(100);        
        //agar kolom terakhir pada tabel bisa auto resize
        tabelTamu.setAutoResizeMode(JTable.AUTO_RESIZE_LAST_COLUMN);
    }
    
    private String tanggal(){
        //instansiasi kelas Date
        Date date = new Date();
        //atur format tanggal yang dinginkan
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        //return value = tanggal hari ini dengan format "hari/bulan/tahun"
        return sdf.format(date);
    }

